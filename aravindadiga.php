About Aravind

Aravind Adiga was born in 1974 in Madras (now Chennai) and grew up in Mangalore in the south of India. He was educated at Columbia University
 in New York and Magdalen College, Oxford. His articles have appeared in publications including the New Yorker, the Sunday Times, the Financial Times,
 and the Times of India. His first novel, The White Tiger, won the Man Booker Prize for Fiction in 2008. He is also the author of Last Man in Tower and
 Selection Day.
