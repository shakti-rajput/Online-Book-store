The Lost Symbol is a 2009 novel written by American writer Dan Brown.[2][3] It is a thriller set in Washington, D.C., after the events of The Da
 Vinci Code, and relies on Freemasonry for both its recurring theme and its major characters.[4] Released on September 15, 2009, it is the third 
 Brown novel to involve the character of Harvard University symbologist Robert Langdon, following 2000's Angels & Demons and 2003's The Da Vinci
 Code.[2] It had a first printing of 6.5 million (5 million in North America, 1.5 million in the UK), the largest in Doubleday history. On its 
 first day the book sold one million in hardcover and e-book versions in the U.S., the UK and Canada, making it the fastest selling adult novel
 in history.[5] It was number one on the New York Times Best Seller list for hardcover fiction[6] for the first six weeks of its release,[7] and 
 remained on the list for 29 weeks. As of January 2013, there were 30 million copies in print worldwide. The Lost Symbol had been in development 
 for several years; originally expected in 2006, the projected publication date was pushed back multiple times.[14] When officially announced, the
 hardcopy book was on pre-order lists for months leading up to its release, being heavily ordered both in the United States and Canada.[15][16] The 
 book was published on September 15, 2009 with an initial print run of 6.5 million copies, the largest first printing in publisher Random House's
 history.[17][18] Electronic versions such as eBook[19] and Audible book versions[20] were also made available on the same date.[21] The American
 release audio book was read by Paul Michael, who also performed the audio book for The Da Vinci Code. The book immediately broke sales records,
